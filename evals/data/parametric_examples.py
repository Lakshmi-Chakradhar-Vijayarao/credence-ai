"""
data/parametric_examples.py
============================
Deterministic parametric expansion of DPO training triples.

No API calls. Each scenario type is hand-written; examples are generated by
substituting parameter combinations. Guarantees:
  - Domain balance: ~830 examples per domain across 6 domains
  - 100% pass rate on verify_triple (explicit qualifier words in faithful_summary)
  - No template collapse: each scenario type has varied system names, values, contexts
  - Qualifier diversity: 6 different qualifier phrases per template
"""

from __future__ import annotations
import itertools
import random
import uuid
from typing import Any

# ---------------------------------------------------------------------------
# Qualifier phrase pools — all appear in _FAITHFULNESS_MARKERS_LOWER
# ---------------------------------------------------------------------------
_QUAL_EXPLICIT = [
    "unverified", "unconfirmed", "not confirmed", "not yet confirmed",
    "approximately", "roughly", "probably", "maybe", "might be",
    "preliminary", "assumed", "reportedly", "estimate",
    "haven't confirmed", "I believe", "I think", "uncertain",
    "not validated", "subject to change", "pending verification",
    "awaiting confirmation",
]

_GHOST_QUAL = [
    "unverified", "unconfirmed", "not confirmed", "not validated",
    "assumed", "reportedly", "subject to change", "awaiting confirmation",
]


# ---------------------------------------------------------------------------
# Helper: expand a template dict into TripleExample-ready dicts
# ---------------------------------------------------------------------------

def _expand(
    template: dict[str, Any],
    qualifier_pool: list[str] | None = None,
) -> list[dict]:
    """
    Expand a scenario template into a list of example dicts.

    template keys:
      domain, qualifier_type, is_ghost,
      conv_template:       str with {param_*} + optional {qualifier}
      faithful_template:   str with {param_*} + {qualifier}
      unfaithful_template: str with {param_*}  (NO qualifier)
      variants:            list of dicts — each a full parameter set
    """
    pool = qualifier_pool if qualifier_pool else (
        _GHOST_QUAL if template.get("is_ghost") else _QUAL_EXPLICIT
    )
    examples = []
    for variant in template["variants"]:
        for qual in pool[:6]:  # max 6 qualifier phrases per variant
            v = dict(variant)
            v["qualifier"] = qual
            try:
                conv = template["conv_template"].format(**v)
                faithful = template["faithful_template"].format(**v)
                unfaithful = template["unfaithful_template"].format(**v)
            except KeyError:
                continue
            # Sanity: unfaithful must not contain qualifier words
            u_lo = unfaithful.lower()
            if any(q in u_lo for q in pool):
                continue
            # Sanity: faithful must contain qualifier
            if qual.lower() not in faithful.lower():
                continue
            examples.append({
                "id": str(uuid.uuid4()),
                "input_conversation": conv.strip(),
                "faithful_summary": faithful.strip(),
                "unfaithful_summary": unfaithful.strip(),
                "source_scenario_id": template["scenario_id"],
                "qualifier_type": template["qualifier_type"],
                "domain": template["domain"],
                "is_ghost": template["is_ghost"],
            })
    return examples


