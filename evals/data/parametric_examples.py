def build_parametric_examples(n: int = 5000, seed: int = 42) -> list[dict]:
    """
    Generate up to n DPO training examples by expanding parametric templates.
    Returns a list of dicts matching TripleExample field names.
    Guaranteed 100% verify_triple pass rate.
    Domain-balanced: each domain gets at most ceil(n / n_domains) examples.
    """
    from collections import defaultdict
    import math
    random.seed(seed)

    all_examples: list[dict] = []
    for template in _ALL_TEMPLATES:
        expanded = _expand(template)
        all_examples.extend(expanded)

    print(f"  Parametric expansion: {len(all_examples)} raw examples from "
          f"{len(_ALL_TEMPLATES)} templates")

    # Group by domain
    by_domain: dict[str, list] = defaultdict(list)
    for ex in all_examples:
        by_domain[ex["domain"]].append(ex)

    domains = sorted(by_domain.keys())
    n_domains = len(domains)
    target_per_domain = math.ceil(n / n_domains)

    print(f"  Domains: {domains}, target per domain: {target_per_domain}")

    # Sample each domain up to target_per_domain
    balanced: list[dict] = []
    for d in domains:
        pool = list(by_domain[d])
        random.shuffle(pool)
        take = pool[:target_per_domain]
        # If a domain has fewer examples than target, cycle with different qualifier
        if len(take) < target_per_domain:
            qualifier_pool = _QUAL_EXPLICIT + _GHOST_QUAL
            idx = 0
            while len(take) < target_per_domain:
                base = dict(pool[idx % len(pool)])
                idx += 1
                new_qual = qualifier_pool[idx % len(qualifier_pool)]
                old_faithful = base["faithful_summary"]
                for q in qualifier_pool:
                    if q in old_faithful.lower():
                        replaced = old_faithful.replace(q, new_qual, 1)
                        if new_qual.lower() in replaced.lower():
                            base = dict(base)
                            base["faithful_summary"] = replaced
                            base["id"] = str(uuid.uuid4())
                            take.append(base)
                        break
        balanced.extend(take)

    random.shuffle(balanced)
    result = balanced[:n]

    # Final distribution
    from collections import Counter
    dist = Counter(e["domain"] for e in result)
    print("  Final domain distribution:", dict(sorted(dist.items())))
    return result


